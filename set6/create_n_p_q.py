from gmpy2 import next_prime 
from random import randint 

while True : 
    p = next_prime(randint(2**384,2**385)) 
    q = next_prime(randint(2**383,2**384))  
    if (p-1) % 3 != 0 and (q-1) % 3 != 0 : 
        n = p * q 
        break 
print(p) 
print(q)
print(n)
print(len(bin(n)[2:]))
